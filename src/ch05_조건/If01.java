package ch05_조건;

public class If01 {

	public static void main(String[] args) {
		
		if (5 > 10) {
			System.out.println("10이 5보다 큽니다.");
			System.out.println("참일 때 출력");
		} else {
			System.out.println("거짓일 때 동작");
			System.out.println("거짓");
		}
		
		// 삼항 연산자와 비교? 같은 값을 가져온다면 연산자가 조금더 연산하기 좋다

	}

}

